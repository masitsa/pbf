					<h4>Who is Privatebushflights?</h4>
                    
                    <p>Privatebushflights is an African company founded by aviation professionals with over 30 years combined industry experience. We realised a need to offer a well established, centralized location for aircraft charter operators to offer their empty positioning flights for sale to the general public or to other charter operators or brokers. </p>

                    <h4>Who benefits from Privatebushflights service?</h4>
                    
                   <p><strong>The Customer</strong> Customers can access a range of flights going to locations not serviced by regular airlines, at a price much cheaper than a normal charter flight. Privatebushflights also makes flying on charter aircraft, more affordable to an average person. </p>
<p><strong>The Aircraft Operator</strong> Charter operators earn extra revenue for their empty flights for little to no additional cost. </p>

                    
                    <h4>Why should I sign up as Operator with Privatebushflights?</h4>
                    <p>Operators can post their empty flights on Privatebushflights free of charge and gain additional revenue. It is free and easy to sign up and start listing your flights. There is no hidden catch! </p>
                    
                    <h4>How will you market and sell the flights to the general public?</h4>
                    <p>Privatebushflights is putting considerably time and funding into marketing the system to the general public, so that it will be successful and beneficial, and that your flights will have a high chance of selling. This includes advertising campaigns in print media such as magazines and newspapers (aviation, travel and general), television and radio advertising, press releases and online marketing (search engines etc). </p>
                    
                    <h4>What commission does Privatebushflights receive for the flight?</h4>
                    <p>The Privatebushflights web system retains a commission of 14%. This covers our overheads especially Marketing and maintaining the system plus allowance for payment processing. </p>
                    
                    <h4>How does the customer pay for a flight?</h4>
                    <p>Customers pay for the flight by credit card or mobile money upon booking through our secure payment processor.</p>
                    
                    <h4>Can the customer book a single seat on a flight?</h4>
                    <p>When a customer purchases a flight with Privatebushflights, they have the option of chartering the whole aircraft for that particular leg. As a licensed travel agent, Private Bush Flights enables Operators to also offer the option of selling individual seats on charter flights or tours.</p>
                    
                    <h4>How do I get paid for the flight?</h4>
                    <p>Privatebushflights will forward the payment to you by EFT within 3 days or less of payment by the customer or at least 24 hrs before operation of the flight. </p>
                    
                    <h4>What happens once a flight is booked?</h4>
                    <p>After the customer books a flight, they are automatically sent a confirmation and your terms of operation, arrival times, baggage allowance and any other necessary information needed by them to facilitate the carriage. You are automatically sent a purchase order and contact details for the passengers should you need to communicate directly. You can manage all your bookings when you login into your privatebushflights account.</p>
                    
                    <h4>What if we need to cancel a booked empty leg?</h4>
                    <p>If the empty leg needs to be cancelled please notify us immediately. We will then notify the customer and provide them with a full refund for the flight. </p>
                    
                    <h4>Can passengers book a single seat on a flight?</h4>
                    <p>Yes. When you are posting a flight, you are provided the option of per seat or per whole plane pricing.</p>
                    
                    <h4>Who is responsible for the flight?</h4>
                    <p>All Privatebushflights flights are operated only by Operators approved by the respective Civil Aviation Safety Authority or other relevant body to conduct charter operations in the relevant state, territory or country. Privatebushflights reviews operator's qualifications when they register with us, however the safety and regulatory compliance of the flight remains with the Operator.</p>
                    
                    <h4>What type of aircraft does Privatebushflights offer?</h4>
                    <p>All types of aircraft that are approved for passenger charter operations can be listed on Privatebushflights. This includes private jets, light aircraft, helicopters and airliners.</p>
                    
                    <h4>Can the customer cancel the flight after booking?</h4>
                    <p>The customer can cancel the flight after booking if necessary, however cancellation fees will apply to cover credit card fees etc.</p>
                    
                    <h4>What flexibility is offered with timing of the flight?</h4>
                    <p>You can arrange with the customer to vary your departure time if required,and conversely, they may request a change, however the final decision is left to your discretion as the operator.</p>
                    
                    <h4>What flexibility is offered with destinations?</h4>
                    <p>Once the customer has purchased a flight, they may request different departure/destinations details to the original booking. You may change the details of the flight at your discretion of the operator. This may incur additional costs which would be arranged from the customer directly with you the operator.</p>
                    
                    <h4>What if the passengers are late?</h4>
                    <p>If the passengers are running late, they are advised to notify you with the contact numbers provided. If the passengers arrive after the arranged departure time they may be required to forfeit the flight without refund.</p>
                    
                    <h4>Can charter brokers post flights on Privatebushflights?</h4>
                    <p>Charter brokers can sign up as an operator also and post empty legs. We recommend liaising with the operator of the flight to ensure they are in agreement with this.</p>

